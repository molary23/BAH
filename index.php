<script type="text/javascript">
$(document).ready(function(){	
	var carouselContainer = $('#home_carousel');
var slideInterval = 5000;

	function toggleC(){
		$('.toggleCaption').hide()
		var caption = carouselContainer.find('.active').find('.toggleCaption').addClass('animated fadeInLeft').one('webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend',
			function (){
			$(this).removeClass('animated fadeInLeft')
	});
	caption.slideToggle();
		$('.carBtn').hide()
		var captionButton = carouselContainer.find('.active').find('.carBtn').addClass('animated fadeInUp').one('webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend',
			function (){
			$(this).removeClass('animated fadeInUp')
		});
	captionButton.slideToggle();
	}
	
	function toggleH(){
		$('.toggleHeading').hide()
		var caption = carouselContainer.find('.active').find('.toggleHeading').addClass('animated fadeInRight').one('webkitAnimationEnd mozAnimationEnd MSAnimationEnd oanimationend animationend',
			function (){
			$(this).removeClass('animated fadeInRight')
		});
	caption.slideToggle();
	}


var moveCar = carouselContainer.carousel({
interval: slideInterval, cycle: true, pause: "hover"});
moveCar.on('slide.bs.carousel slid.bs.carousel', toggleH).trigger('slide.bs.carousel');
moveCar.on('slide.bs.carousel slid.bs.carousel', toggleC).trigger('slide.bs.carousel');

$('#home_carousel .carousel-control').hover(function(){
	$('#home_carousel .carousel-control').addClass('control-round');
});

    moveCar.on("slide.bs.carousel", function(event) {
        //The animate class gets removed so that it jumps straight back to 0%
        $(".transition-timer-carousel-progress-bar", this)
            .removeClass("animate").css("width", "0%");
    }).on("slid.bs.carousel", function(event) {
        //The slide transition finished, so re-add the animate class so that
        //the timer bar takes time to fill up
        $(".transition-timer-carousel-progress-bar", this)
            .addClass("animate").css("width", "100%");
    });
    
    //Kick off the initial slide animation when the document is ready
    $(".transition-timer-carousel-progress-bar", "#home_carousel")
        .css("width", "100%");

        $('.home_equip .carousel[data-type="multi"] .item').each(function() {
	var next = $(this).next();
	if (!next.length) {
		next = $(this).siblings(':first');
	}
	next.children(':first-child').clone().appendTo($(this));

	for (var i = 0; i < 2; i++) {
		next = next.next();
		if (!next.length) {
			next = $(this).siblings(':first');
		}

		next.children(':first-child').clone().appendTo($(this));
	}
});

  function openVideo($button, $imageDiv, $videoName){
  	  $($button).click(function(){
  	  	$('.home_videos video').removeAttr('autoplay');
  	  	$('.home_videos video').hide();
  		$('.home_vid').show();
  		$($imageDiv).hide();
  		$($videoName).attr('autoplay', 'autoplay');
  		$($videoName).show();
  		return false;
  });
  }

 openVideo(('#playRack'), ('.home_rack_video'), ('#rackVideo'));
 openVideo(('#playShelves'), ('.home_shelves_video'), ('#shelvesVideo'));
 openVideo(('#playEquip'), ('.home_equip_video'), ('#equipVideo'));
});
</script>
